            Long labelId = Long.valueOf(jLabel_id_procesoDoblado.getText() );
            String cboLocal = jComboBox_local_procesoDoblado.getSelectedItem().toString();
            String txtOrden1 = jTextField_numOrden1_procesoDoblado.getText();
            String txtCant1 = !"".equals(jTextField_cantPrendas1_procesoDoblado.getText()) ? jTextField_cantPrendas1_procesoDoblado.getText() : "0";
