    private void cargarTabla_TabDoblado() {
        defaultTableModel_Doblado = new DefaultTableModel() {
                @Override
                public boolean isCellEditable(int row, int column) {
                    return false; // Hace que todas las celdas sean no editables
                }

                @Override
                public Class<?> getColumnClass(int columnIndex) {
                    if (columnIndex == 11) { // La columna "Estado" que tiene booleanos es la columna 12
                        return Boolean.class;
                    }
                    return super.getColumnClass(columnIndex);
                }
        };
        
        defaultTableModel_Doblado.addColumn("N°");
        defaultTableModel_Doblado.addColumn("Fecha");
        defaultTableModel_Doblado.addColumn("Hora");
        defaultTableModel_Doblado.addColumn("Local");
        defaultTableModel_Doblado.addColumn("N° Orden 1");
        defaultTableModel_Doblado.addColumn("Cant Prendas 1");
        defaultTableModel_Doblado.addColumn("N° Orden 2");
        defaultTableModel_Doblado.addColumn("Cant Prendas 2");
        defaultTableModel_Doblado.addColumn("N° Orden 3");
        defaultTableModel_Doblado.addColumn("Cant Prendas 3");
        defaultTableModel_Doblado.addColumn("Responsable");
        defaultTableModel_Doblado.addColumn("Estado");
        List<Operacion> allOperacionesDoblado = operacionDao.getOperacionesByType("Doblado");
        
        //CARGA DE OPERACIONES SECADO 
        if(!allOperacionesDoblado.isEmpty()){
             for (Operacion op : allOperacionesDoblado) {
                // Verificar que haya al menos un orden en la lista
                String numOrden1 = !op.getOrdenes().isEmpty() ? op.getOrdenes().get(0).getNumOrden() : "No encontrado";
                String cantPrendas1 = !op.getOrdenes().isEmpty() ? String.valueOf(op.getOrdenes().get(0).getCantPrendas() ): "" ;

                // Verificar que haya al menos dos órdenes en la lista
                String numOrden2 = op.getOrdenes().size() > 1 ? op.getOrdenes().get(1).getNumOrden() : "No  Encontrado";
                String cantPrendas2 = op.getOrdenes().size() > 1 ? String.valueOf(op.getOrdenes().get(1).getCantPrendas()) : "" ;

                // Verificar que haya al menos tres órdenes en la lista
                String numOrden3 = op.getOrdenes().size() > 2 ? op.getOrdenes().get(2).getNumOrden() : "No  Encontrado";
                String cantPrendas3 = op.getOrdenes().size() > 2 ? String.valueOf(op.getOrdenes().get(2).getCantPrendas()) : "";

                String responsableName = op.getResponsable() != null ? op.getResponsable().getNombres() + "," + op.getResponsable().getApellidos(): "";
                
                
                String fecha = op.getFecha() != null ? op.getFecha().toLocalDate().toString(): ""; 
                String hora = op.getFecha() != null ? op.getFecha().toLocalTime().toString(): "";
                String local = op.getLocal() != null ? op.getLocal().getNombre(): "";
                
                Boolean estado = op.getEstadoOperacion() != null ? op.getEstadoOperacion() : false;
                // Añadir fila a la tabla con datos verificados
                defaultTableModel_Doblado.addRow(
                    new Object[]{
                        op.getId().toString(),
                        fecha,
                        hora,
                        local,
                        numOrden1,
                        cantPrendas1,
                        numOrden2,
                        cantPrendas2,
                        numOrden3,
                        cantPrendas3,
                        responsableName,
                        estado
                    }
                );
            }
            
        }

        jTable_procesoDoblado.setModel(defaultTableModel_Doblado);
        jTable_procesoDoblado.getColumnModel().getColumn(0).setPreferredWidth(5);
        
        
        
    }
    
    private void limpiarCamposTabDoblado(){
        // Obtener el valor de la primera columna y la última fila
        int lastRowIndex = jTable_procesoDoblado.getRowCount() - 1;
        if (lastRowIndex >= 0) {
            Object value = jTable_procesoDoblado.getValueAt(lastRowIndex, 0);
            //Convertir a String
            String valueStr = String.valueOf(value);
            
            //Convertir a Integer e incrementar
            int valueInt = Integer.parseInt(valueStr) +  1;
            
            //Asignar al jLabel
            jLabel_id_procesoDoblado.setText(String.valueOf(valueInt));
           
        } else {
            jLabel_id_procesoDoblado.setText("0");
        }
        
        jComboBox_local_procesoDoblado.removeAllItems();
        
        
        jComboBox_responsable_procesoDoblado.removeAllItems();
        
        jTextField_numOrden1_procesoDoblado.setText("");
        jTextField_cantPrendas1_procesoDoblado.setText("");
        jTextField_numOrden2_procesoDoblado.setText("");
        jTextField_cantPrendas2_procesoDoblado.setText("");
        jTextField_numOrden3_procesoDoblado.setText("");
        jTextField_cantPrendas3_procesoDoblado.setText("");
        jCheckBox_estado_procesoDoblado.setSelected(false);
        cargarComboBoxesTabDoblado();
    }

    private void cargarComboBoxesTabDoblado(){
         List<Empleado> empleados = empleadoDAO.getAllEmpleados();
        List<Local> locales = localDAO.getAllLocales();
        
        empleados.forEach(emp -> jComboBox_responsable_procesoDoblado.addItem(emp.getNombres()+ "," + emp.getApellidos()));
        locales.forEach(local -> jComboBox_local_procesoDoblado.addItem(local.getNombre()) );
        
    }
